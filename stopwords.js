/**
 * Remove all stop words from a string or array.
 *
 * @param string/array of words to check
 * @return string/array, return type dependent on input type
 */
var removeStopWords = function(words) {
  var cleanWords = [], inputString = false;

  // convert types between arrays & strings
  if (!$.isArray(words)) {
    words = words.split(' ');
    inputString = true;
  }
  // check each word against stopwords arra
  jQuery.each(words, function(i, val) {
    if (!isStopWord(val)) {
      cleanWords.push(val);
    }
  });

  // if string was inputed, return a string
  if (inputString) {
    cleanWords = cleanWords.join(' ');
  }

  return cleanWords;
};

/**
 * Determine whether a word is a stopword or not.
 *
 * @param word to test
 * @return boolean
 */
var isStopWord = function(word) {
  word = jQuery.trim(word);
  return (jQuery.inArray(word, stopWords) > -1);
};



